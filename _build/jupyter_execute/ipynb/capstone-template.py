# ## Orientações Gerais
# 
# ### Definição 
# 
# O Projeto Final de Visualização de Dados (PFVD) é uma tarefa desempenhada de forma individual ou grupal (no máximo 4 integrantes) que aplica todo o conhecimento adquirido no curso para a entrega de dois ativos: i) relatório formal e ii) produto de visualização material.
# 
# ### Objetivo 
# 
# Empregar o conhecimento adquirido para elaborar um produto de visualização útil a uma audiência pré-selecionada cuja materialização dá-se, mas não se limita, por:
# - relatórios ou laudos automatizados; ou
# - projetos visuais aplicados ou _dashboards_; ou
# - ideação de gráficos ou elementos visuais inovadores.
# 
# ### Escopo
# 
# O PFVD deverá se sustentar pela base de conhecimento adquirida a partir dos Módulos 1 a 3 do curso, bem como pela disponível em fontes externas.
# 
# ### Tema
# 
# Livre.
# 
# ### Ativos
# 
# #### Relatório formal
# 
# O documento formal (relatório) que sustentará a base de conhecimento teórico-metodológica do PFVD deverá ser redigido em até 4 páginas, contendo as seguintes seções:
# 
#  - _Escopo_: campo de conhecimento científico ou domínio de expertise no qual o projeto se insere;
#  - _Desafio_: descrição do desafio (científico, tecnológico, operacional etc.) que o projeto busca solucionar;
#  - _Projeto de visualização_: descrição das etapas a serem tomadas para atingimento da representação visual (documento descritivo do modelo referencial);
#  - _Mapeamento_: codificação resumida utilizada para o mapeamento dos dados de entrada;
#  - _Representação(ões) visual(is)_ (RV(s)): plotagem(ns) obtida(s) após a execução do previsto no projeto de visualização;
#  - _Análise de dados_: discussão resumida da leitura analítica que pode ser feita a partir da(s) RV(s);
#  - _Takeaways_: síntese do mini-projeto em três pontos de destaque principais ("mensagem a ser levada para casa");
#  - _Caracterização do produto mínimo viável_: síntese das funções práticas do produto de visualização proposto.
# 
# #### Produto de visualização material
# 
# - O produto material e suas funcionalidades será demonstrado por meio de apresentação (em até 12 minutos) para a classe.
#  
# ### Nota
# 
# No PFVD, a ênfase está na **entrega de um produto de visualização** que demonstre de maneira clara uma oferta de solução para o desafio proposto.
